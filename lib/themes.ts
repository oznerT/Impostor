export function getRandomWords(words: string[], count: number): string[] {
  const shuffled = [...words].sort(() => 0.5 - Math.random())
  const selectedWord = shuffled[0]
  return Array(count).fill(selectedWord)
}

export function getThemeById(id: string): Theme | undefined {
  return themes.find((theme) => theme.id === id)
}

export function getSubThemeWords(themeId: string, subThemeId: string): string[] {
  const theme = getThemeById(themeId)
  if (!theme) return []

  if (subThemeId === "general") {
    return theme.words
  }

  return theme.subThemes?.[subThemeId]?.words ?? theme.words
}
